def red(msg):
    return "\033[0;31m{0}\033[0m".format(msg)


def green(msg):
    return "\033[0;32m{0}\033[0m".format(msg)


def orange(msg):
    return "\033[0;33m{0}\033[0m".format(msg)


def blue(msg):
    return "\033[0;34m{0}\033[0m".format(msg)


def purple(msg):
    return "\033[0;35m{0}\033[0m".format(msg)


def cyan(msg):
    return "\033[0;36m{0}\033[0m".format(msg)